    public void readPtable(){
        ProdutoDao pdao = new ProdutoDao();
        DefaultTableModel modelo = (DefaultTableModel)tabelaCliente.getModel();
        modelo.setNumRows(0);
        
        for(Produto p: pdao.read()){
            modelo.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getQtd(),
                p.getPreco()
            });
        }
        
    }
    
    

