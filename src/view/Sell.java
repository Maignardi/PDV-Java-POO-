    public void readVtable(){
        VendasDao Vdao = new VendasDao();
        DefaultTableModel modelo = (DefaultTableModel)tabelaVendas.getModel();
        modelo.setNumRows(0);
        
        for(Vendas v: Vdao.read()){
            modelo.addRow(new Object[]{
                v.getId(),
                v.getProduto(),
                v.getCliente(),
                v.getQtd(),
                v.getPreco()
            });
        }
        
    }

