    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed

        if(tabelaVendas.getSelectedRow()!= -1);
        
        Vendas v = new Vendas();
        VendasDao dao = new VendasDao();
        
        v.setProduto(txtProduto.getText());
        v.setCliente(txtCliente.getText());
        v.setQtd(Integer.parseInt(txtQtd.getText()));
        v.setPreco(Double.parseDouble(txtValor.getText()));
        v.setId((int)tabelaVendas.getValueAt(tabelaVendas.getSelectedRow(), 0));
        dao.update(v);
        readVtable();

    }//GEN-LAST:event_btAtualizarActionPerformed

    private void tabelaVendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaVendasMouseClicked

        int posicaoLinha = tabelaVendas.getSelectedRow();

        int codigoCliente = Integer.parseInt(tabelaVendas.getValueAt(posicaoLinha, 0).toString());

        txtProduto.setText(tabelaVendas.getValueAt(posicaoLinha, 1).toString());
        txtCliente.setText(tabelaVendas.getValueAt(posicaoLinha, 2).toString());
        txtQtd.setText(tabelaVendas.getValueAt(posicaoLinha, 3).toString());
        txtValor.setText(tabelaVendas.getValueAt(posicaoLinha, 4).toString());
        
    }//GEN-LAST:event_tabelaVendasMouseClicked

